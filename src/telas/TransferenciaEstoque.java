        double estoqueSaida = 0.0;
        double estoqueFinal = 0.0;

        boolean status = movimentacaoEstoqueDao.conectar();
        int resposta;
        java.sql.Date sqlDate = null;

        try {
            id = Integer.parseInt(txtID.getText());
            strData = txtData.getText();
            data = LocalDate.parse(strData, fmt);
            idProdutoAcabado = Integer.parseInt(txtIdProdutoAcabado.getText());
            quantidadeAcabado = Double.parseDouble(txtQuantidadeAcabado.getText());
            idProdutoCongelado = Integer.parseInt(txtIdProdutoCongelado.getText());
            estoqueInicial = Double.parseDouble(txtEstoqueInicial.getText());
            estoqueSaida = Double.parseDouble(txtQuantidadeSaida.getText());
            estoqueFinal = Double.parseDouble(txtEstoqueFinal.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Formato InvÃ¡lido!");
            return;
        }
        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro de conexÃ£o");
        } else {
            SimpleDateFormat conversor = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Date dataConvertida = conversor.parse(strData);
                sqlDate = new java.sql.Date(dataConvertida.getTime());
            } catch (ParseException e) {
                System.out.println("Erro na conversÃ£o da data");
            }
            ProdutoCongelado congelado = new ProdutoCongelado();
            congelado.setId(idProdutoCongelado);

            ProdutoAcabado acabado = new ProdutoAcabado();
            acabado.setId(idProdutoAcabado);
            MovimentacaoEstoque estoque = new MovimentacaoEstoque(id, quantidadeAcabado, estoqueInicial, estoqueSaida, estoqueFinal, acabado, congelado, sqlDate);

            resposta = movimentacaoEstoqueDao.salvarTransferencia(estoque);

            if (resposta == 1) {
                JOptionPane.showMessageDialog(rootPane, "Entrada realizada com sucesso!");
            } else if (resposta == 1062) {
                JOptionPane.showMessageDialog(null, "Dados jÃ¡ cadastrado");
            }
            jbSalvar.setEnabled(false);
            movimentacaoEstoqueDao.desconectar();
        }
        limparTela();

