    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        int id = 0;
        boolean status = movimentacaoEstoqueDao.conectar();
        try {
            id = Integer.parseInt(txtId.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Formato Inválido!");
        }

        if (status == true) {
            MovimentacaoEstoque estoque = movimentacaoEstoqueDao.consultarCongelado(id);
            if (estoque == null) {
                JOptionPane.showMessageDialog(null, "Não foi encontrado uma movimentação com ID " + id + "");
            } else {
                txtOperacao.setText(Integer.toString(estoque.getOperacao()));
                txtOperacao.setEditable(false);
                txtOperacaoNome.setText(estoque.getOperacaoNome());
                txtOperacaoNome.setEditable(false);
                txtData.setText(this.reverterData(String.valueOf(estoque.getData())));
                txtData.setEditable(false);
                txtIdFornecedor.setText(Integer.toString(estoque.getFornecedor().getId()));
                txtIdFornecedor.setEditable(false);
                txtNomeFornecedor.setText(estoque.getFornecedor().getNomeEmpresa());
                txtNomeFornecedor.setEditable(false);
                txtIdProdutoCongelado.setText(Integer.toString(estoque.getProdutoCongelado().getId()));
                txtIdProdutoCongelado.setEditable(false);
                txtNomeProdutoCongelado.setText(estoque.getProdutoCongelado().getNome());
                txtNomeProdutoCongelado.setEditable(false);
                txtQuantidade.setText(Double.toString(estoque.getQuantidade()));
                txtQuantidade.setEditable(false);
                jbExcluir.setEnabled(true);
                return;
            }
            movimentacaoEstoqueDao.desconectar();
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        Object[] options = {"OK", "CANCELAR"};
        int id = 0;
        boolean status = movimentacaoEstoqueDao.conectar();
        try {
            id = Integer.parseInt(txtId.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Formato Inválido");
        }
        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        } else {
            MovimentacaoEstoque movimentacaoEstoque = movimentacaoEstoqueDao.consultarCongelado(id);
            if (movimentacaoEstoque == null) {
                JOptionPane.showMessageDialog(rootPane, "Movimentação com ID: " + id + " não encontrado");
            } else {
                int confirmação = JOptionPane.showOptionDialog(null, "Deseja excluir a Movimentação: \nID:" + movimentacaoEstoque.getId() + "\nNome do Produto: " + movimentacaoEstoque.getProdutoCongelado().getNome() + "\nOperação: " + movimentacaoEstoque.getOperacaoNome() + "\nQuantidade: " + movimentacaoEstoque.getQuantidade(),
                        "Aviso", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);
                if (confirmação == 0) {
                    status = movimentacaoEstoqueDao.excluir(id);
                    JOptionPane.showMessageDialog(rootPane, "Movimentação: " + id + "\nNome do Produto: " + movimentacaoEstoque.getProdutoCongelado().getNome() + "\nOperação: " + movimentacaoEstoque.getOperacaoNome() + "\nQuantidade: " + movimentacaoEstoque.getQuantidade()
                            + "\n\nRemovido com sucesso!");
                    limparTela();
                    movimentacaoEstoqueDao.desconectar();
                    return;
                } else if (confirmação == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Exclusão cancelada");
                    movimentacaoEstoqueDao.desconectar();
                    return;
                }
            }
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

