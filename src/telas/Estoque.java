    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        int id = 0;
        int operacao = 0;
        String strData = "";
        LocalDate data = this.data;
        int idFornecedor = 0;
        String nomeFornecedor = "";
        int idProduto = 0;
        String nomeProduto = "";
        double quantidade = 0.0;
        String operacaoNome = "";
        boolean status = movimentacaoEstoqueDao.conectar();
        int resposta;
        java.sql.Date sqlDate = null;

        try {
            id = Integer.parseInt(txtId.getText());
            operacao = Integer.parseInt(txtOperacao.getText());
            strData = txtData.getText();
            data = LocalDate.parse(strData, fmt);
            idFornecedor = Integer.parseInt(txtIdFornecedor.getText());
            nomeFornecedor = txtNomeFornecedor.getText();
            idProduto = Integer.parseInt(txtIdProdutoCongelado.getText());
            nomeProduto = txtNomeProdutoCongelado.getText();
            quantidade = Double.parseDouble(txtQuantidade.getText());
            operacaoNome = txtOperacaoNome.getText();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Preencha os campos corretamente!");
            return;
        }

        if (!nomeFornecedor.equals("") && !nomeProduto.equals("")) {
            if (operacao == 1) {
                if (status == false) {
                    JOptionPane.showMessageDialog(null, "Erro de conexão");
                } else {

                    SimpleDateFormat conversor = new SimpleDateFormat("dd/MM/yyyy");
                    try {
                        Date dataConvertida = conversor.parse(strData);
                        sqlDate = new java.sql.Date(dataConvertida.getTime());
                    } catch (ParseException e) {
                        System.out.println("Erro na conversão da data");
                    }
                    Fornecedor fornecedor = new Fornecedor();
                    fornecedor.setId(idFornecedor);
                    ProdutoCongelado congelado = new ProdutoCongelado();
                    congelado.setId(idProduto);
                    MovimentacaoEstoque estoque = new MovimentacaoEstoque(id, quantidade, operacao, operacaoNome, fornecedor, congelado, sqlDate);
                    resposta = movimentacaoEstoqueDao.salvarCongelado(estoque);

                    if (resposta == 1) {
                        JOptionPane.showMessageDialog(rootPane, "Entrada realizada com sucesso!");
                    } else if (resposta == 1062) {
                        JOptionPane.showMessageDialog(null, "Dados já cadastrado");
                    }
                    jbSalvar.setEnabled(false);
                    movimentacaoEstoqueDao.desconectar();
                }

            } else if (operacao == 2) {
                if (status == false) {
                    JOptionPane.showMessageDialog(null, "Erro de conexão");
                } else {

                    SimpleDateFormat conversor = new SimpleDateFormat("dd/MM/yyyy");
                    try {
                        Date dataConvertida = conversor.parse(strData);
                        sqlDate = new java.sql.Date(dataConvertida.getTime());
                    } catch (ParseException e) {
                        System.out.println("Erro na conversão da data");
                    }
                    Fornecedor fornecedor = new Fornecedor();
                    fornecedor.setId(idFornecedor);
                    ProdutoCongelado congelado = new ProdutoCongelado();
                    congelado.setId(idProduto);
                    MovimentacaoEstoque estoque = new MovimentacaoEstoque(id, quantidade, operacao, operacaoNome, fornecedor, congelado, sqlDate);
                    resposta = movimentacaoEstoqueDao.salvarCongelado(estoque);

                    if (resposta == 1) {
                        JOptionPane.showMessageDialog(rootPane, "Saída realizada com sucesso!");
                    } else if (resposta == 1062) {
                        JOptionPane.showMessageDialog(null, "Dados já cadastrado");
                    }
                    jbSalvar.setEnabled(false);
                    movimentacaoEstoqueDao.desconectar();
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Preencha os campos corretamente");
                return;
            }
            limparTela();
            txtId.setEditable(true);
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

